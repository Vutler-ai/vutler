#!/usr/bin/env node
'use strict';

require('dotenv').config({ path: '.env.local', quiet: true, override: true });
require('dotenv').config({ quiet: true, override: true });

const { Client } = require('pg');
const { URL } = require('url');
const {
  getDatabaseUrl,
  getDatabaseHost,
  getDatabasePort,
  getDatabaseName,
  getDatabaseUser,
  getDatabasePassword,
} = require('../lib/database-env');

function parseArgs(argv) {
  const args = {
    schema: 'tenant_vutler',
    targetOwner: 'tenant_vutler_owner',
    grantRoles: ['tenant_vutler', 'tenant_vutler_service'],
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--schema' && argv[i + 1]) args.schema = argv[++i];
    if (arg === '--target-owner' && argv[i + 1]) args.targetOwner = argv[++i];
    if (arg === '--grant-role' && argv[i + 1]) args.grantRoles.push(argv[++i]);
  }

  args.grantRoles = Array.from(new Set(args.grantRoles.filter(Boolean)));
  return args;
}

function getClientConfig() {
  const databaseUrl = getDatabaseUrl();
  if (databaseUrl) {
    const parsed = new URL(databaseUrl);
    return {
      host: parsed.hostname,
      port: parseInt(parsed.port || '5432', 10),
      database: parsed.pathname.replace(/^\//, '').split('?')[0],
      user: decodeURIComponent(parsed.username),
      password: decodeURIComponent(parsed.password),
      ssl: false,
    };
  }

  return {
    host: getDatabaseHost(),
    port: getDatabasePort(),
    database: getDatabaseName(),
    user: getDatabaseUser(),
    password: getDatabasePassword(),
    ssl: false,
  };
}

function ident(value) {
  return `"${String(value).replace(/"/g, '""')}"`;
}

function relkindToAlter(relkind) {
  return {
    r: 'TABLE',
    p: 'TABLE',
    S: 'SEQUENCE',
    v: 'VIEW',
    m: 'MATERIALIZED VIEW',
    f: 'FOREIGN TABLE',
  }[relkind] || null;
}

async function expandGrantRoles(client, grantRoles) {
  const expandedRoles = new Set((grantRoles || []).filter(Boolean));

  for (const role of [...expandedRoles]) {
    const matchingRoles = await client.query(
      `SELECT rolname
         FROM pg_roles
        WHERE rolname = $1
           OR rolname LIKE $2
        ORDER BY rolname ASC`,
      [role, `${role}.%`]
    );

    for (const row of matchingRoles.rows) {
      expandedRoles.add(row.rolname);
    }
  }

  return [...expandedRoles].sort((left, right) => left.localeCompare(right));
}

async function main(argv = process.argv.slice(2)) {
  const args = parseArgs(argv);
  const client = new Client(getClientConfig());
  await client.connect();

  try {
    const objects = await client.query(
      `SELECT c.relkind,
              c.relname AS object_name,
              pg_get_userbyid(c.relowner) AS owner
         FROM pg_class c
         JOIN pg_namespace n ON n.oid = c.relnamespace
        WHERE n.nspname = $1
          AND c.relkind IN ('r', 'p', 'S', 'v', 'm', 'f')
          AND NOT (
            c.relkind = 'S'
            AND EXISTS (
              SELECT 1
              FROM pg_depend d
              WHERE d.objid = c.oid
                AND d.deptype IN ('a', 'i')
            )
          )
        ORDER BY c.relkind, c.relname`,
      [args.schema]
    );

    const functions = await client.query(
      `SELECT p.proname AS object_name,
              pg_get_function_identity_arguments(p.oid) AS identity_args,
              pg_get_userbyid(p.proowner) AS owner
         FROM pg_proc p
         JOIN pg_namespace n ON n.oid = p.pronamespace
        WHERE n.nspname = $1
        ORDER BY p.proname, identity_args`,
      [args.schema]
    );

    const grantRoles = await expandGrantRoles(client, args.grantRoles);

    const lines = [];
    lines.push(`-- Owner normalization for schema ${args.schema}`);
    lines.push(`-- Generated by scripts/generate-owner-normalization-sql.js`);
    lines.push('BEGIN;');
    lines.push('');
    lines.push('DO $$');
    lines.push('BEGIN');
    lines.push(`  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = '${args.targetOwner.replace(/'/g, "''")}') THEN`);
    lines.push(`    CREATE ROLE ${ident(args.targetOwner)} NOLOGIN;`);
    lines.push('  END IF;');
    lines.push('END $$;');
    lines.push('');
    lines.push(`ALTER SCHEMA ${ident(args.schema)} OWNER TO ${ident(args.targetOwner)};`);
    lines.push('');

    for (const row of objects.rows) {
      const alterType = relkindToAlter(row.relkind);
      if (!alterType) continue;
      lines.push(`ALTER ${alterType} ${ident(args.schema)}.${ident(row.object_name)} OWNER TO ${ident(args.targetOwner)};`);
    }

    if (functions.rows.length > 0) {
      lines.push('');
      for (const row of functions.rows) {
        lines.push(
          `ALTER FUNCTION ${ident(args.schema)}.${ident(row.object_name)}(${row.identity_args}) OWNER TO ${ident(args.targetOwner)};`
        );
      }
    }

    if (grantRoles.length > 0) {
      lines.push('');
      for (const role of grantRoles) {
        lines.push(`GRANT USAGE ON SCHEMA ${ident(args.schema)} TO ${ident(role)};`);
        lines.push(`GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA ${ident(args.schema)} TO ${ident(role)};`);
        lines.push(`GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA ${ident(args.schema)} TO ${ident(role)};`);
        lines.push(`GRANT EXECUTE ON ALL FUNCTIONS IN SCHEMA ${ident(args.schema)} TO ${ident(role)};`);
      }

      lines.push('');
      for (const role of grantRoles) {
        lines.push(
          `ALTER DEFAULT PRIVILEGES FOR ROLE ${ident(args.targetOwner)} IN SCHEMA ${ident(args.schema)} GRANT SELECT, INSERT, UPDATE, DELETE ON TABLES TO ${ident(role)};`
        );
        lines.push(
          `ALTER DEFAULT PRIVILEGES FOR ROLE ${ident(args.targetOwner)} IN SCHEMA ${ident(args.schema)} GRANT USAGE, SELECT ON SEQUENCES TO ${ident(role)};`
        );
        lines.push(
          `ALTER DEFAULT PRIVILEGES FOR ROLE ${ident(args.targetOwner)} IN SCHEMA ${ident(args.schema)} GRANT EXECUTE ON FUNCTIONS TO ${ident(role)};`
        );
      }
    }

    lines.push('');
    lines.push('COMMIT;');
    console.log(lines.join('\n'));
  } finally {
    await client.end().catch(() => {});
  }
}

if (require.main === module) {
  main().catch((error) => {
    console.error('[OWNERS] SQL generation failed:', error.message || String(error));
    process.exitCode = 1;
  });
}

module.exports = {
  expandGrantRoles,
  getClientConfig,
  parseArgs,
  relkindToAlter,
};
